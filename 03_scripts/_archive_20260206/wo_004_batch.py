def process_item(item, dry_run=False):
    """ë‹¨ì¼ ì•„ì´í…œ ì²˜ë¦¬: ë Œë”ë§ + ìº¡ì…˜ + ë©”íƒ€ë°ì´í„°"""
    food_id = item["food_id"]
    food_ko = item["food_ko"]
    safety = item["safety"]
    cover_file = item.get("cover_file", "")
    folder_name = item.get("folder", "")
    body1 = item["body1"]
    body2 = item["body2"]
    cta = item["cta"]
    cover_title = item.get("cover_title", food_id.upper())

    result = {
        "food_id": food_id,
        "food_ko": food_ko,
        "safety": safety.upper(),
        "text_json": False,
        "render_01": False,
        "render_02": False,
        "render_03": False,
        "caption_insta": False,
        "caption_threads": False,
        "metadata": False,
        "validators": "N/A",
        "errors": [],
        "skipped": False,
    }

    # í´ë” ì°¾ê¸°
    folder = find_folder(food_id)
    if not folder:
        # ì»¤ë²„ ì†ŒìŠ¤ë¡œ í´ë” ìƒì„± ì‹œë„
        cover_src = find_cover_source(food_id, cover_file)
        if not cover_src:
            result["errors"].append(f"í´ë” ì—†ìŒ + ì»¤ë²„ì†ŒìŠ¤ ì—†ìŒ")
            result["skipped"] = True
            return result

        # ë²ˆí˜¸ ì¶”ì¶œ
        num = cover_src.stem.split("_")[1] if "_" in cover_src.stem else "999"
        folder_name_new = f"{int(num):03d}_{food_id}_{food_ko}"
        folder = COVER_ONLY_DIR / folder_name_new
        folder.mkdir(parents=True, exist_ok=True)
        (folder / "archive").mkdir(exist_ok=True)
        print(f"    ìƒˆ í´ë” ìƒì„±: {folder_name_new}")

    folder_name = folder.name

    # body_readyë¡œ ì´ë™ í•„ìš” ì—¬ë¶€
    if COVER_ONLY_DIR in folder.parents or folder.parent == COVER_ONLY_DIR:
        dest = BODY_READY_DIR / folder.name
        if not dest.exists():
            shutil.move(str(folder), str(dest))
            folder = dest
            print(f"    â†’ 2_body_ready/ ì´ë™")
        else:
            folder = dest

    archive = folder / "archive"
    archive.mkdir(exist_ok=True)

    # ì»¤ë²„ ì†ŒìŠ¤ í™•ì¸
    cover_src = find_cover_source(food_id, cover_file)
    if not cover_src:
        result["errors"].append(f"ì»¤ë²„ì†ŒìŠ¤ ì—†ìŒ: {cover_file}")
        result["skipped"] = True
        return result

    # [1] text.json ìƒì„±
    try:
        save_text_json(food_id, cover_title, body1, body2, cta)
        result["text_json"] = True
    except Exception as e:
        result["errors"].append(f"text.json ì‹¤íŒ¨: {e}")

    validators_passed = 0
    validators_total = 0

    # [2-4] ë Œë”ë§ (ì´ë¯¸ ìˆìœ¼ë©´ ìŠ¤í‚µ)
    for slide_num, slide_type, text_data in [
        ("01", "body", body1),
        ("02", "body", body2),
    ]:
        out_file = folder / f"{food_id}_{slide_num}.png"
        if out_file.exists() and os.path.getsize(out_file) > 10000:
            result[f"render_{slide_num}"] = True
            print(f"    _{slide_num} ì´ë¯¸ ì¡´ì¬ â†’ ìŠ¤í‚µ")
            validators_passed += 3
            validators_total += 3
            continue

        try:
            bg_file = archive / f"{food_id}_{slide_num}_bg.png"
            if not bg_file.exists():
                shutil.copy2(cover_src, bg_file)

            v_config = build_validation_config(slide_type, safety)
            rel_path = str(bg_file.relative_to(PROJECT_ROOT))
            validate_before_render(slide_type, rel_path, v_config)
            validators_passed += 1
            validators_total += 1

            img = Image.open(bg_file).resize(TARGET_SIZE, Image.LANCZOS)
            title_clean = strip_emoji(text_data["title"])
            sub_clean = strip_emoji(text_data["subtitle"])
            img = render_body(img, title_clean, sub_clean, safety)
            img.save(out_file, "PNG")

            validate_v31_slide(slide_type, v_config, str(out_file.relative_to(PROJECT_ROOT)))
            validators_passed += 2
            validators_total += 2

            result[f"render_{slide_num}"] = True
            print(f"    _{slide_num} ë Œë”ë§ ì™„ë£Œ ({os.path.getsize(out_file)//1024}KB)")
        except Exception as e:
            validators_total += 2
            result["errors"].append(f"_{slide_num} ì‹¤íŒ¨: {e}")
            print(f"    _{slide_num} ì‹¤íŒ¨: {e}")

    # CTA ë Œë”ë§
    out_03 = folder / f"{food_id}_03.png"
    cta_src_file = ""
    if out_03.exists() and os.path.getsize(out_03) > 10000:
        result["render_03"] = True
        print(f"    _03 ì´ë¯¸ ì¡´ì¬ â†’ ìŠ¤í‚µ")
        validators_passed += 3
        validators_total += 3
    else:
        try:
            cta_src = select_cta_image(food_id)
            cta_src_file = cta_src.name
            bg_03 = archive / f"{food_id}_03_bg.png"
            shutil.copy2(cta_src, bg_03)

            v_config = build_validation_config("cta", safety)
            rel_path = str(bg_03.relative_to(PROJECT_ROOT))
            validate_before_render("cta", rel_path, v_config)
            validators_passed += 1
            validators_total += 1

            img = Image.open(bg_03).resize(TARGET_SIZE, Image.LANCZOS)
            title_clean = strip_emoji(cta["title"])
            sub_clean = strip_emoji(cta["subtitle"])
            img = render_cta(img, title_clean, sub_clean, bg_path=rel_path)
            img.save(out_03, "PNG")

            validate_v31_slide("cta", v_config, str(out_03.relative_to(PROJECT_ROOT)))
            validators_passed += 2
            validators_total += 2

            result["render_03"] = True
            print(f"    _03 CTA ì™„ë£Œ ({os.path.getsize(out_03)//1024}KB)")
        except Exception as e:
            validators_total += 2
            result["errors"].append(f"_03 CTA ì‹¤íŒ¨: {e}")
            print(f"    _03 CTA ì‹¤íŒ¨: {e}")

    result["validators"] = f"{validators_passed}/{validators_total}"

    # [5] ìº¡ì…˜ ìƒì„± (í•­ìƒ ì¬ìƒì„±)
    try:
        insta_caption = generate_instagram_caption(food_id, food_ko, safety, body1, body2)
        with open(folder / "caption_instagram.txt", "w", encoding="utf-8") as f:
            f.write(insta_caption)
        result["caption_insta"] = True
    except Exception as e:
        result["errors"].append(f"ì¸ìŠ¤íƒ€ ìº¡ì…˜ ì‹¤íŒ¨: {e}")

    try:
        threads_caption = generate_threads_caption(food_id, food_ko, safety, body1, body2)
        with open(folder / "caption_threads.txt", "w", encoding="utf-8") as f:
            f.write(threads_caption)
        result["caption_threads"] = True
    except Exception as e:
        result["errors"].append(f"ì“°ë ˆë“œ ìº¡ì…˜ ì‹¤íŒ¨: {e}")

    # [6] ë©”íƒ€ë°ì´í„° ìƒì„±
    try:
        slides_rendered = sum([
            result["render_01"], result["render_02"], result["render_03"]
        ])
        meta = generate_metadata(
            food_id, food_ko, safety, folder_name,
            slides_rendered, cover_src.name if cover_src else "",
            cta_src_file,
        )
        with open(folder / "metadata.json", "w", encoding="utf-8") as f:
            json.dump(meta, f, ensure_ascii=False, indent=2)
        result["metadata"] = True
    except Exception as e:
        result["errors"].append(f"ë©”íƒ€ë°ì´í„° ì‹¤íŒ¨: {e}")

    return result


